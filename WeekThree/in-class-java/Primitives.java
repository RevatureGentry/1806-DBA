class TheseAreThePrimitivesInJava {

	// There are 8 primitives in Java 
	// This is an example of camelCase
	
	// Otherwise specified, these are the default values
	static boolean myBoolean;	// default to false
	static int myInt;			// default to 0
	static byte myByte;			// default to 0
	static short myShort;		// default to 0
	static long myLong;			// default to 0
	static float myFloat;		// default to 0.0
	static double myDouble;		// default to 0.0
	static char myChar;			// default to 0
	
	static Object myObject;		// Anything else defaults 
								// to null
	
	public static void main(String[] argzzz) {
		System.out.println("myBoolean = " + myBoolean);
		System.out.println("myByte = " + myByte);
		System.out.println("myShort = " + myShort);
		System.out.println("myChar = " + myChar);
		System.out.println("myInt = " + myInt);
		System.out.println("myLong = " + myLong);
		System.out.println("myFloat = " + myFloat);
		System.out.println("myDouble = " + myDouble);
		System.out.println("myObject = " + myObject);
	}
}








